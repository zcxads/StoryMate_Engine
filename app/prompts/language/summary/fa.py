"""
Persian summary prompt
"""

from langchain_core.prompts import PromptTemplate

PERSIAN_SUMMARY_PROMPT = PromptTemplate(
    input_variables=["book_content"],
    partial_variables={"page_count": ""},
    template="""شما یک خلاصه‌نویس حرفه‌ای محتوا هستید. باید فقط به فارسی پاسخ دهید.

**محتوای مورد نیاز برای خلاصه:**
{book_content}

**بحرانی: باید به فارسی پاسخ دهید. به زبان دیگری ترجمه نکنید.**

**دستورالعمل‌های خلاصه:**
1. **دستورالعمل‌های طول**:
   - برای 1-5 صفحه: 2-3 جمله
   - برای 6-15 صفحه: 4-6 جمله (1-2 پاراگراف)
   - برای 16-30 صفحه: 6-10 جمله (2-3 پاراگراف)
   - برای بیش از 30 صفحه: 8-12 جمله (3-4 پاراگراف)
   - برای محتوای وب (صفحه واحد): 3-8 جمله بسته به طول محتوا

2. **دستورالعمل‌های محتوا**:
   - موضوعات اصلی، رویدادهای کلیدی و پیام‌های مهم را خلاصه کنید
   - جزئیات خاص و مثال‌ها را در صورت مرتبط بودن شامل شوید
   - لحن و سبک محتوای اصلی را حفظ کنید
   - نظرات یا تحلیل‌ها را اضافه نکنید مگر اینکه در اصل باشند
   - جامع اما مختصر باشید

**محتوای فعلی {page_count} صفحه دارد.**

**خلاصه:**""")
