"""
Hindi summary prompt
"""

from langchain_core.prompts import PromptTemplate

HINDI_SUMMARY_PROMPT = PromptTemplate(
    input_variables=["book_content"],
    partial_variables={"page_count": ""},
    template="""आप एक पेशेवर सामग्री सारांशकर्ता हैं। आपको केवल हिंदी में उत्तर देना चाहिए।

**सारांशित करने के लिए सामग्री:**
{book_content}

**महत्वपूर्ण: आपको हिंदी में उत्तर देना चाहिए। किसी अन्य भाषा में अनुवाद न करें।**

**सारांश दिशानिर्देश:**
1. **लंबाई दिशानिर्देश**:
   - 1-5 पृष्ठों के लिए: 2-3 वाक्य
   - 6-15 पृष्ठों के लिए: 4-6 वाक्य (1-2 पैराग्राफ)
   - 16-30 पृष्ठों के लिए: 6-10 वाक्य (2-3 पैराग्राफ)
   - 30 से अधिक पृष्ठों के लिए: 8-12 वाक्य (3-4 पैराग्राफ)
   - वेब सामग्री (एकल पृष्ठ) के लिए: सामग्री की लंबाई के आधार पर 3-8 वाक्य

2. **सामग्री दिशानिर्देश**:
   - मुख्य विषयों, प्रमुख घटनाओं और महत्वपूर्ण संदेशों का सारांश दें
   - प्रासंगिक होने पर विशिष्ट विवरण और उदाहरण शामिल करें
   - मूल सामग्री के स्वर और शैली को बनाए रखें
   - मूल में न होने पर टिप्पणी या विश्लेषण न जोड़ें
   - व्यापक लेकिन संक्षिप्त रहें

**वर्तमान सामग्री में {page_count} पृष्ठ हैं।**

**सारांश:**""")
