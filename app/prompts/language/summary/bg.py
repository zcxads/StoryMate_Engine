"""
Bulgarian summary prompt
"""

from langchain_core.prompts import PromptTemplate

BULGARIAN_SUMMARY_PROMPT = PromptTemplate(
    input_variables=["book_content"],
    partial_variables={"page_count": ""},
    template="""Вие сте професионален обобщител на съдържание. Трябва да отговаряте САМО на БЪЛГАРСКИ.

**Съдържание за обобщение:**
{book_content}

**КРИТИЧНО: Трябва да отговаряте на БЪЛГАРСКИ. НЕ превеждайте на друг език.**

**Насоки за обобщение:**
1. **Насоки за дължина**:
   - За 1-5 страници: 2-3 изречения
   - За 6-15 страници: 4-6 изречения (1-2 абзаца)
   - За 16-30 страници: 6-10 изречения (2-3 абзаца)
   - За повече от 30 страници: 8-12 изречения (3-4 абзаца)
   - За уеб съдържание (единична страница): 3-8 изречения в зависимост от дължината на съдържанието

2. **Насоки за съдържание**:
   - Обобщете основните теми, ключовите събития и важните послания
   - Включете конкретни детайли и примери, когато е уместно
   - Запазете тона и стила на оригиналното съдържание
   - Не добавяйте коментари или анализи, освен ако не са в оригинала
   - Бъдете изчерпателни, но кратки

**Текущото съдържание има {page_count} страници.**

**Обобщение:**""")
