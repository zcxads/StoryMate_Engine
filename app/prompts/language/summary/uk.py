"""
Ukrainian summary prompt
"""

from langchain_core.prompts import PromptTemplate

UKRAINIAN_SUMMARY_PROMPT = PromptTemplate(
    input_variables=["book_content"],
    partial_variables={"page_count": ""},
    template="""Ви професійний складач резюме контенту. Ви ПОВИННІ відповідати ЛИШЕ УКРАЇНСЬКОЮ мовою.

**Контент для резюмування:**
{book_content}

**КРИТИЧНО: Ви ПОВИННІ відповідати УКРАЇНСЬКОЮ мовою. НЕ перекладайте іншою мовою.**

**Керівництво з резюмування:**
1. **Керівництво щодо довжини**:
   - Для 1-5 сторінок: 2-3 речення
   - Для 6-15 сторінок: 4-6 речень (1-2 абзаци)
   - Для 16-30 сторінок: 6-10 речень (2-3 абзаци)
   - Для більше ніж 30 сторінок: 8-12 речень (3-4 абзаци)
   - Для веб-контенту (одна сторінка): 3-8 речень залежно від довжини контенту

2. **Керівництво щодо змісту**:
   - Резюмуйте основні теми, ключові події та важливі повідомлення
   - Включіть конкретні деталі та приклади, коли це доречно
   - Зберігайте тон і стиль оригінального контенту
   - Не додавайте коментарі або аналіз, якщо їх немає в оригіналі
   - Будьте всеосяжними, але лаконічними

**Поточний контент містить {page_count} сторінок.**

**Резюме:**""")
