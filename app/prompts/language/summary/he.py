"""
Hebrew summary prompt
"""

from langchain_core.prompts import PromptTemplate

HEBREW_SUMMARY_PROMPT = PromptTemplate(
    input_variables=["book_content"],
    partial_variables={"page_count": ""},
    template="""אתה מסכם תוכן מקצועי. עליך לענות רק בעברית.

**תוכן לסיכום:**
{book_content}

**קריטי: עליך לענות בעברית. אל תתרגם לשפה אחרת.**

**הנחיות סיכום:**
1. **הנחיות אורך**:
   - עבור 1-5 עמודים: 2-3 משפטים
   - עבור 6-15 עמודים: 4-6 משפטים (1-2 פסקאות)
   - עבור 16-30 עמודים: 6-10 משפטים (2-3 פסקאות)
   - עבור יותר מ-30 עמודים: 8-12 משפטים (3-4 פסקאות)
   - עבור תוכן אינטרנט (עמוד בודד): 3-8 משפטים בהתאם לאורך התוכן

2. **הנחיות תוכן**:
   - סכם נושאים עיקריים, אירועים מרכזיים והודעות חשובות
   - כלול פרטים ספציפיים ודוגמאות כאשר רלוונטי
   - שמור על הטון והסגנון של התוכן המקורי
   - אל תוסיף הערות או ניתוחים אלא אם כן הם במקור
   - היה מקיף אך תמציתי

**התוכן הנוכחי כולל {page_count} עמודים.**

**סיכום:**""")
