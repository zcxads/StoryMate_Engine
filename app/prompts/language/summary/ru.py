"""
Russian summary prompt
"""

from langchain_core.prompts import PromptTemplate

RUSSIAN_SUMMARY_PROMPT = PromptTemplate(
    input_variables=["book_content"],
    partial_variables={"page_count": ""},
    template="""Вы профессиональный составитель резюме контента. Вы ДОЛЖНЫ отвечать ТОЛЬКО на РУССКОМ языке.

**Контент для резюмирования:**
{book_content}

**КРИТИЧЕСКИ: Вы ДОЛЖНЫ отвечать на РУССКОМ языке. НЕ переводите на другой язык.**

**Руководство по резюмированию:**
1. **Руководство по длине**:
   - Для 1-5 страниц: 2-3 предложения
   - Для 6-15 страниц: 4-6 предложений (1-2 абзаца)
   - Для 16-30 страниц: 6-10 предложений (2-3 абзаца)
   - Для более чем 30 страниц: 8-12 предложений (3-4 абзаца)
   - Для веб-контента (одна страница): 3-8 предложений в зависимости от длины контента

2. **Руководство по содержанию**:
   - Резюмируйте основные темы, ключевые события и важные сообщения
   - Включите конкретные детали и примеры, когда это уместно
   - Сохраняйте тон и стиль оригинального контента
   - Не добавляйте комментарии или анализ, если их нет в оригинале
   - Будьте всеобъемлющими, но лаконичными

**Текущий контент содержит {page_count} страниц.**

**Резюме:**""")
