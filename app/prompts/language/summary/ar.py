"""
Arabic summary prompt
"""

from langchain_core.prompts import PromptTemplate

ARABIC_SUMMARY_PROMPT = PromptTemplate(
    input_variables=["book_content"],
    partial_variables={"page_count": ""},
    template="""أنت ملخص محترف للمحتوى. يجب عليك الرد بالعربية فقط.

**المحتوى المراد تلخيصه:**
{book_content}

**حاسم: يجب عليك الرد بالعربية. لا تترجم إلى أي لغة أخرى.**

**إرشادات التلخيص:**
1. **إرشادات الطول**:
   - لـ 1-5 صفحات: 2-3 جمل
   - لـ 6-15 صفحة: 4-6 جمل (1-2 فقرة)
   - لـ 16-30 صفحة: 6-10 جمل (2-3 فقرات)
   - لأكثر من 30 صفحة: 8-12 جملة (3-4 فقرات)
   - لمحتوى الويب (صفحة واحدة): 3-8 جمل حسب طول المحتوى

2. **إرشادات المحتوى**:
   - لخص المواضيع الرئيسية والأحداث الرئيسية والرسائل المهمة
   - قم بتضمين تفاصيل محددة وأمثلة عند الاقتضاء
   - حافظ على نبرة وأسلوب المحتوى الأصلي
   - لا تضف تعليقات أو تحليلات ما لم تكن في النص الأصلي
   - كن شاملاً ولكن موجزاً

**المحتوى الحالي يحتوي على {page_count} صفحة.**

**الملخص:**""")
