"""
日本語要約プロンプト
"""

from langchain_core.prompts import PromptTemplate

JAPANESE_SUMMARY_PROMPT = PromptTemplate(
    input_variables=["book_content"],
    partial_variables={"page_count": ""},
    template="""あなたは専門的な内容要約者です。以下の内容を日本語で要約してください。

**要約する内容：**
{book_content}

**必須言語ルール:**
- 必ず日本語のみで応答してください
- 他の言語に翻訳しないでください
- 出力の100%が日本語でなければなりません

**要約ガイドライン：**
1. **長さガイドライン**：
   - 1-5ページ：2-3文
   - 6-15ページ：4-6文（1-2段落）
   - 16-30ページ：6-10文（2-3段落）
   - 30ページ以上：8-12文（3-4段落）
   - ウェブコンテンツ（単一ページ）：内容の長さに応じて3-8文程度

2. **内容ガイドライン**：
   - 主要なテーマ、重要な出来事、重要なメッセージを要約
   - 関連する具体的な詳細と例を含める
   - 原文のトーンとスタイルを維持
   - 原文にない解説や分析は追加しない
   - 包括的だが簡潔に

**現在の内容は{page_count}ページです。**

**要約：**""")
