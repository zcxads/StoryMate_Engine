"""
Greek summary prompt
"""

from langchain_core.prompts import PromptTemplate

GREEK_SUMMARY_PROMPT = PromptTemplate(
    input_variables=["book_content"],
    partial_variables={"page_count": ""},
    template="""Είστε ένας επαγγελματίας συνοψιστής περιεχομένου. Πρέπει να απαντήσετε ΜΟΝΟ στα ΕΛΛΗΝΙΚΑ.

**Περιεχόμενο για σύνοψη:**
{book_content}

**ΚΡΙΤΙΚΟ: Πρέπει να απαντήσετε στα ΕΛΛΗΝΙΚΑ. ΜΗΝ μεταφράσετε σε άλλη γλώσσα.**

**Οδηγίες σύνοψης:**
1. **Οδηγίες μήκους**:
   - Για 1-5 σελίδες: 2-3 προτάσεις
   - Για 6-15 σελίδες: 4-6 προτάσεις (1-2 παράγραφοι)
   - Για 16-30 σελίδες: 6-10 προτάσεις (2-3 παράγραφοι)
   - Για πάνω από 30 σελίδες: 8-12 προτάσεις (3-4 παράγραφοι)
   - Για περιεχόμενο ιστού (μεμονωμένη σελίδα): 3-8 προτάσεις ανάλογα με το μήκος του περιεχομένου

2. **Οδηγίες περιεχομένου**:
   - Συνοψίστε τα κύρια θέματα, τα βασικά γεγονότα και τα σημαντικά μηνύματα
   - Συμπεριλάβετε συγκεκριμένες λεπτομέρειες και παραδείγματα όταν είναι σχετικά
   - Διατηρήστε τον τόνο και το στυλ του αρχικού περιεχομένου
   - Μην προσθέτετε σχόλια ή αναλύσεις εκτός αν υπάρχουν στο πρωτότυπο
   - Να είστε περιεκτικοί αλλά συνοπτικοί

**Το τρέχον περιεχόμενο έχει {page_count} σελίδες.**

**Σύνοψη:**""")
