"""
한국어 요약 프롬프트
"""

from langchain_core.prompts import PromptTemplate

KOREAN_SUMMARY_PROMPT = PromptTemplate(
    input_variables=["book_content"],
    partial_variables={"page_count": ""},
    template="""당신은 전문적인 내용 요약가입니다. 다음 내용을 한국어로 요약해주세요.

**요약할 내용:**
{book_content}

**필수 언어 규칙:**
- 반드시 한국어로만 응답하세요
- 다른 언어로 번역하지 마세요
- 출력의 100%가 한국어여야 합니다

**요약 가이드라인:**
1. **길이 가이드라인**:
   - 1-5페이지: 2-3문장
   - 6-15페이지: 4-6문장 (1-2단락)
   - 16-30페이지: 6-10문장 (2-3단락)
   - 30페이지 이상: 8-12문장 (3-4단락)
   - 웹 콘텐츠 (단일 페이지): 내용 길이에 따라 3-8문장

2. **내용 가이드라인**:
   - 주요 테마, 핵심 사건, 중요한 메시지를 요약
   - 관련된 구체적인 세부사항과 예시 포함
   - 원본 내용의 톤과 스타일 유지
   - 원본에 없는 해설이나 분석은 추가하지 않음
   - 포괄적이지만 간결하게

**현재 내용은 {page_count}페이지입니다.**

**요약:**""")
